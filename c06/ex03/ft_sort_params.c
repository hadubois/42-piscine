#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
			i++;
	}
}

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i])
		i++;
	return (s1[i] - s2[i]);
}

int	main(int ac, char **av)
{
	int	i;

	i = 1;
	while (i < ac - 1)
	{
		if (ft_strcmp(av[i], av[i + 1]) > 0)
		{
			av[0] = av[i];
			av[i] = av[i + 1];
			av[i + 1] = av[0];
			i = 0;
		}
		i ++;
	}
	i = 1;
	while (av[i])
	{
		ft_putstr(av[i]);
		write (1, "\n", 1);
		i++;
	}
}
